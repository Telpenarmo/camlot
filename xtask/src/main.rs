use std::{env, ffi::OsString, path::PathBuf};

use anyhow::Result;
use xshell::Shell;

mod install;
mod sourcegen;

fn main() -> Result<()> {
    let mode = App::from_env()?.subcommand;

    let sh = Shell::new()?;
    sh.change_dir(project_root());

    match mode {
        AppCmd::Generate(_) => sourcegen::generate_ungrammar(),
        AppCmd::Install(opts) => install::install(&sh, &opts),
    }
}

xflags::xflags! {
    src "./src/main.rs"
    cmd app {
        /// Generate AST from ungrammar definition
        cmd generate {}
        /// Install the language server and vscode extension
        cmd install {
            /// Do not install the language server
            optional --skip-server
            /// Do not install the vscode extension
            optional --skip-extension
            /// The path to the vscode executable, defaults to 'code'
            optional --code-path path: OsString
        }
    }
}
// generated start
// The following code is generated by `xflags` macro.
// Run `env UPDATE_XFLAGS=1 cargo build` to regenerate.
#[derive(Debug)]
pub struct App {
    pub subcommand: AppCmd,
}

#[derive(Debug)]
pub enum AppCmd {
    Generate(Generate),
    Install(Install),
}

#[derive(Debug)]
pub struct Generate;

#[derive(Debug)]
pub struct Install {
    pub skip_server: bool,
    pub skip_extension: bool,
    pub code_path: Option<OsString>,
}

impl App {
    #[allow(dead_code)]
    pub fn from_env_or_exit() -> Self {
        Self::from_env_or_exit_()
    }

    #[allow(dead_code)]
    pub fn from_env() -> xflags::Result<Self> {
        Self::from_env_()
    }

    #[allow(dead_code)]
    pub fn from_vec(args: Vec<std::ffi::OsString>) -> xflags::Result<Self> {
        Self::from_vec_(args)
    }
}
// generated end

fn project_root() -> PathBuf {
    let dir =
        env::var("CARGO_MANIFEST_DIR").unwrap_or_else(|_| env!("CARGO_MANIFEST_DIR").to_owned());
    PathBuf::from(dir).parent().unwrap().to_owned()
}
